    function createPrompt(newsText, socialNetworks, otherName) {
    let prompt = "Напиши короткий и привлекательный текст для поста в социальной сети на основе следующей новости:\n\n" + newsText + "\n\n";

    if (socialNetworks.includes("twitter")) {
        prompt += "Сгенерируй текст для твита (Twitter). Уложись в 280 символов, используй релевантные хэштеги. Текст должен быть максимально ёмким и информативным.\n---\n";
    }
    if (socialNetworks.includes("youtube")) {
        prompt += "Сгенерируй текст для описания к видео на YouTube.  Текст должен быть информативным, привлекать внимание и содержать ключевые слова для поиска. Можно добавить таймкоды, если новость связана с конкретными моментами.\n---\n";
    }
    if (socialNetworks.includes("tiktok")) {
        prompt += "Сгенерируй текст для описания к видео в TikTok. Используй короткие, цепляющие фразы, популярные хэштеги и призывы к действию (например, посмотреть видео). Можно добавить эмодзи.\n---\n";
    }
    if (socialNetworks.includes("instagram")) {
        prompt += "Сгенерируй текст для поста в Instagram.  Используй привлекательные фразы, релевантные хэштеги (не слишком много).  Текст должен соответствовать визуальному контенту. Можно добавить эмодзи.\n---\n";
    }
    if (socialNetworks.includes("telegram")) {
        prompt += "Сгенерируй текст для поста в Telegram. Используй сдержанный, информативный тон. Можно добавить несколько релевантных хэштегов.\n---\n";
    }
    if (socialNetworks.includes("facebook")) {
        prompt += "Сгенерируй текст для поста в Facebook.  Текст должен быть понятным и вовлекающим.  Можно использовать как короткие, так и более развернутые предложения.\n---\n";
    }
    if (socialNetworks.includes("other")) {
        prompt += `Сгенерируй текст для поста в ${otherName}. Учти особенности этой платформы.\n---\n`;
    }

    prompt += "\nМаксимальная длина каждого поста разумно ограничена.";
    return prompt;
}
